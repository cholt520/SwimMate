package entity;

public class Facility {
	private int id;
	private String showers;
	private int beachid;
	private String pools;
	private String cafe;
	private String parks;
	private String boat_ramp;
	private String carparking;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getShowers() {
		return showers;
	}
	public void setShowers(String showers) {
		this.showers = showers;
	}
	public int getBeachid() {
		return beachid;
	}
	public void setBeachid(int beachid) {
		this.beachid = beachid;
	}
	public String getPools() {
		return pools;
	}
	public void setPools(String pools) {
		this.pools = pools;
	}
	public String getCafe() {
		return cafe;
	}
	public void setCafe(String cafe) {
		this.cafe = cafe;
	}
	public String getParks() {
		return parks;
	}
	public void setParks(String parks) {
		this.parks = parks;
	}
	public String getBoat_ramp() {
		return boat_ramp;
	}
	public void setBoat_ramp(String boat_ramp) {
		this.boat_ramp = boat_ramp;
	}
	public String getCarparking() {
		return carparking;
	}
	public void setCarparking(String carparking) {
		this.carparking = carparking;
	}
	
	

}
