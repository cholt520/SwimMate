package entity;

public class Training {
	private int id;
	private String name;
	private String address;
	private String suburb;
	private String postcode;
	private String state;
	private String businessCategory;
	private String lga;
	private String region;
	
	/**
	* This is a getter which gets the id
	@param id - the id to be get
	*/
	public int getId() {
		return id;
	}
	
	/**
	* This is a setter which sets the id
	@param id - the id to be set
	*/
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	* This is a getter which gets the name
	@param name - the name to be get
	*/
	public String getName() {
		return name;
	}
	
	/**
	* This is a setter which sets the name
	@param name - the name to be set
	*/
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	* This is a getter which gets the address
	@param address - the address to be get
	*/
	public String getAddress() {
		return address;
	}
	
	/**
	* This is a setter which sets the suburb
	@param suburb - the suburb to be set
	*/
	public void setAddress(String address) {
		this.address = address;
	}
	
	/**
	* This is a getter which gets the suburb
	@param suburb - the suburb to be get
	*/
	public String getSuburb() {
		return suburb;
	}
	
	/**
	* This is a setter which sets the suburb
	@param suburb - the suburb to be set
	*/
	public void setSuburb(String suburb) {
		this.suburb = suburb;
	}
	
	/**
	* This is a getter which gets the postcode
	@param postcode - the postcode to be get
	*/
	public String getPostcode() {
		return postcode;
	}
	
	/**
	* This is a setter which sets the postcode
	@param postcode - the postcode to be set
	*/
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	
	/**
	* This is a getter which gets the state
	@param state - the state to be get
	*/
	public String getState() {
		return state;
	}
	
	/**
	* This is a setter which sets the state
	@param state - the state to be set
	*/
	public void setState(String state) {
		this.state = state;
	}
	
	/**
	* This is a getter which gets the businessCategory
	@param businessCategory - the businessCategory to be get
	*/
	public String getBusinessCategory() {
		return businessCategory;
	}
	
	/**
	* This is a setter which sets the businessCategory
	@param businessCategory - the businessCategory to be set
	*/
	public void setBusinessCategory(String businessCategory) {
		this.businessCategory = businessCategory;
	}
	
	/**
	* This is a getter which gets the lga
	@param lga - the lga to be get
	*/
	public String getLga() {
		return lga;
	}
	
	/**
	* This is a setter which sets the lga
	@param lga - the lga to be set
	*/
	public void setLga(String lga) {
		this.lga = lga;
	}
	
	/**
	* This is a getter which gets the region
	@param region - the region to be get
	*/
	public String getRegion() {
		return region;
	}
	
	/**
	* This is a setter which sets the region
	@param region - the region to be set
	*/
	public void setRegion(String region) {
		this.region = region;
	}
}
